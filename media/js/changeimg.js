$(document).ready(function(){
            /*웹페이지 열었을 때*/
            $(".oi1").show();
            $(".ci1").hide();
 
            /*img:1을 클릭했을 때 img:2를 보여줌*/
            $(".oi1").click(function(){
                $(".oi1").hide();
                $(".ci1").show();
            });
 
            /*img:2를 클릭했을 때 img:1을 보여줌*/
            $(".ci1").click(function(){
                $(".oi1").show();
                $(".ci1").hide();
            });
        });

